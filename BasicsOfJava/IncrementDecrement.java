package BasicsOfJava;

public class IncrementDecrement {
    public static void main(String[] args) {
        int x = 5;
        x++;
        System.out.println(x);
//        6

        x--;
        System.out.println(x);
//      5

        ++x;
        System.out.println(x);
//      6

        --x;
        System.out.println(x);
//        5
    }

}
