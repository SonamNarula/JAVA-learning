package BasicsOfJava;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int x = 5;
        int y = 2;

        System.out.println(x + y); // 7
        System.out.println(x - y); // 3
        System.out.println(x * y); // 10
        System.out.println(x / y); // 2
    }
}
